/**
 * Seed Conversations Script
 * Creates test conversations with multiple messages for pagination testing
 *
 * Usage:
 *   npm run seed [mode] [count] [conversationId]
 *
 * Modes:
 *   batch   - Add messages in batch (fast, no AI responses) - default
 *   real    - Send messages and get real AI responses (slow)
 *
 * Arguments:
 *   mode           - 'batch' or 'real' (default: batch)
 *   count          - Number of message pairs to create (default: 20 for batch, 5 for real)
 *   conversationId - Optional: existing conversation ID to add messages to (must start with conv_)
 *
 * Examples:
 *   npm run seed                           # Batch: 20 message pairs, new conversation
 *   npm run seed batch 50                  # Batch: 50 message pairs, new conversation
 *   npm run seed batch 10 conv_abc123...   # Batch: 10 pairs to existing conversation
 *   npm run seed real 5                    # Real: 5 messages with AI responses
 *   npm run seed real 3 conv_abc123...     # Real: 3 messages to existing conversation
 */

