/**
 * Batch seed: Add messages directly to conversation items (fast, no AI)
 */
async function seedBatch(messageCount: number = 20, existingConversationId?: string) {
  console.log(`\n🌱 Batch seeding with ${messageCount} message pairs...\n`);

  try {
    const openAIClient = await getOpenAIClient();
    let conversationId: string;

    if (existingConversationId) {
      // Validate it's a real Azure conversation ID (not a local fake one)
      if (!existingConversationId.match(/^conv_[a-zA-Z0-9]{40,}/)) {
        console.error(`❌ Invalid conversation ID format: ${existingConversationId}`);
        console.error(`   Azure conversation IDs look like: conv_6277ed9d4779752300PEopnFljAhda5DVgdZGPWZ4wt4gpL2YA`);
        process.exit(1);
      }
      console.log(`📝 Using existing conversation: ${existingConversationId}`);
      conversationId = existingConversationId;
    } else {
      // Create a new conversation via Azure AI
      console.log("📝 Creating new conversation via Azure AI...");
      const conversation = await openAIClient.conversations.create({
        metadata: {
          title: `Test Batch Conversation - ${new Date().toISOString()}`,
          sessionId: "seed-script-batch",
        },
      });
      conversationId = conversation.id;
      
      // Validate the returned ID looks correct
      if (!conversationId.match(/^conv_[a-zA-Z0-9]{40,}/)) {
        console.error(`❌ Azure returned invalid conversation ID: ${conversationId}`);
        console.error(`   This might mean you're running against mock mode or there's an API issue.`);
        process.exit(1);
      }
      console.log(`✅ Created conversation: ${conversationId}`);
    }
    console.log();

    // Create message pairs in batches
    const BATCH_SIZE = 10;
    for (let batch = 0; batch < messageCount; batch += BATCH_SIZE) {
      const batchEnd = Math.min(batch + BATCH_SIZE, messageCount);
      const items: Array<{
        type: "message";
        role: "user" | "assistant";
        content: Array<{ type: "input_text"; text: string }>;
      }> = [];

      for (let i = batch; i < batchEnd; i++) {
        const userMsg = TEST_MESSAGES[i % TEST_MESSAGES.length];
        const assistantMsg = MOCK_RESPONSES[i % MOCK_RESPONSES.length];

        // Add user message
        items.push({
          type: "message",
          role: "user",
          content: [{ type: "input_text", text: `[Test ${i + 1}] ${userMsg}` }],
        });

        // Add assistant message (use input_text for both when adding to items)
        items.push({
          type: "message",
          role: "assistant",
          content: [{ type: "input_text", text: `[Response ${i + 1}] ${assistantMsg}` }],
        });
      }

      console.log(`📤 Adding batch ${batch / BATCH_SIZE + 1}: messages ${batch + 1}-${batchEnd}...`);

      try {
        // Cast to ResponseInputItem[] for SDK compatibility
        await openAIClient.conversations.items.create(conversationId, { 
          items: items as Parameters<typeof openAIClient.conversations.items.create>[1]["items"],
        });
        console.log(`✅ Batch added successfully\n`);
      } catch (err) {
        console.error(`❌ Failed to add batch:`, err);
      }
    }

    console.log(`\n✨ Done! Conversation ID: ${conversationId}`);
    console.log(`   Total messages: ${messageCount * 2} (${messageCount} user + ${messageCount} assistant)\n`);

    return conversationId;
  } catch (error) {
    console.error("❌ Failed to seed conversation:", error);
    process.exit(1);
  }
}

/**
 * Real seed: Send messages and get actual AI responses (slow)
 */
async function seedReal(messageCount: number = 5, existingConversationId?: string) {
  console.log(`\n🌱 Real seeding with ${messageCount} messages (with AI responses)...\n`);

  try {
    const openAIClient = await getOpenAIClient();
    const agentName = getAgentName();
    let conversationId: string;

    if (existingConversationId) {
      // Validate it's a real Azure conversation ID (not a local fake one)
      if (!existingConversationId.match(/^conv_[a-zA-Z0-9]{40,}/)) {
        console.error(`❌ Invalid conversation ID format: ${existingConversationId}`);
        console.error(`   Azure conversation IDs look like: conv_6277ed9d4779752300PEopnFljAhda5DVgdZGPWZ4wt4gpL2YA`);
        process.exit(1);
      }
      console.log(`📝 Using existing conversation: ${existingConversationId}`);
      conversationId = existingConversationId;
    } else {
      // Create a new conversation via Azure AI
      console.log("📝 Creating new conversation via Azure AI...");
      const conversation = await openAIClient.conversations.create({
        metadata: {
          title: `Test Real Conversation - ${new Date().toISOString()}`,
          sessionId: "seed-script-real",
        },
      });
      conversationId = conversation.id;
      
      // Validate the returned ID looks correct
      if (!conversationId.match(/^conv_[a-zA-Z0-9]{40,}/)) {
        console.error(`❌ Azure returned invalid conversation ID: ${conversationId}`);
        console.error(`   This might mean you're running against mock mode or there's an API issue.`);
        process.exit(1);
      }
      console.log(`✅ Created conversation: ${conversationId}`);
    }
    console.log();

    // Send messages one by one
    for (let i = 0; i < messageCount; i++) {
      const message = TEST_MESSAGES[i % TEST_MESSAGES.length];
      const messageNum = i + 1;

      console.log(`📤 [${messageNum}/${messageCount}] Sending: "${message.substring(0, 30)}..."`);

      try {
        const response = await openAIClient.responses.create(
          {
            input: `[Test ${messageNum}] ${message}`,
            conversation: conversationId,
            stream: false,
          },
          { body: { agent: { name: agentName, type: "agent_reference" } } },
        );

        // Extract response text from output_text field
        const responseText = response.output_text || "No response";
        console.log(`📥 [${messageNum}/${messageCount}] Response: "${responseText.substring(0, 50)}..."\n`);

        // Small delay to avoid rate limiting
        await new Promise((resolve) => setTimeout(resolve, 1000));
      } catch (err) {
        console.error(`❌ Failed to send message ${messageNum}:`, err);
      }
    }

    console.log(`\n✨ Done! Conversation ID: ${conversationId}`);
    console.log(`   Total messages: ${messageCount * 2} (${messageCount} user + ${messageCount} assistant)\n`);

    return conversationId;
  } catch (error) {
    console.error("❌ Failed to seed conversation:", error);
    process.exit(1);
  }
}

// Parse arguments
const args = process.argv.slice(2);
const mode = args[0] === "real" ? "real" : "batch";
const countArg = args[mode === "real" ? 1 : args[0] === "batch" ? 1 : 0];
const count = parseInt(countArg || (mode === "real" ? "5" : "20"), 10);
const conversationIdArg = args[mode === "real" ? 2 : args[0] === "batch" ? 2 : 1];
const conversationId = conversationIdArg?.startsWith("conv_") ? conversationIdArg : undefined;

console.log(`Mode: ${mode}, Count: ${count}${conversationId ? `, ConversationId: ${conversationId}` : " (new conversation)"}`);

if (mode === "real") {
  seedReal(count, conversationId);
} else {
  seedBatch(count, conversationId);
}
